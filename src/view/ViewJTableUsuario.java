    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        if (jUsuarios.getSelectedRow() != -1) {

            Usuario c = new Usuario();
            UsuarioDAO dao = new UsuarioDAO();

            c.setId((int) jUsuarios.getValueAt(jUsuarios.getSelectedRow(), 0));
            
            dao.delete(c);

            txtNomecomp.setText("");
            txtNomeusu.setText("");
            txtTelefone.setText("");
            txtEmail.setText("");
            txtSenha.setText("");

            readJTable();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um produto para Excluir");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void jUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jUsuariosMouseClicked
        // TODO add your handling code here:
        if (jUsuarios.getSelectedRow() != -1) {
            txtNomecomp.setText(jUsuarios.getValueAt(jUsuarios.getSelectedRow(), 1).toString());
            txtNomeusu.setText(jUsuarios.getValueAt(jUsuarios.getSelectedRow(), 2).toString());
            txtTelefone.setText(jUsuarios.getValueAt(jUsuarios.getSelectedRow(), 3).toString());
            txtEmail.setText(jUsuarios.getValueAt(jUsuarios.getSelectedRow(), 4).toString());
            txtSenha.setText(jUsuarios.getValueAt(jUsuarios.getSelectedRow(), 5).toString());
        }
    }//GEN-LAST:event_jUsuariosMouseClicked

