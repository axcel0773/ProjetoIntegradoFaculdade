    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jUsuarios.getModel();
        modelo.setNumRows(0);

        UsuarioDAO cdao = new UsuarioDAO();

        for (Usuario c : cdao.read()) {

            modelo.addRow(new Object[]{
                c.getId(),
                c.getNomecomp(),
                c.getNomeusu(),
                c.getTelefone(),
                c.getEmail(),
                c.getSenha()
            });
        }
    }

