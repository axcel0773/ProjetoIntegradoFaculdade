    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jProjetos.getModel();
        modelo.setNumRows(0);

        ProjetoDAO pdao = new ProjetoDAO();

        for (Projeto p : pdao.read()) {

            modelo.addRow(new Object[]{
                p.getId(),
                p.getNome(),
                p.getModulo(),
                p.getFuncionalidade(),
                p.getDatacriacao(),
                p.getAutor(),
                p.getVersao(),
                p.getPrioridade(),
                p.getComplexidade(),
                p.getEsforco(),
                p.getEstado(),
                p.getFase(),
                p.getDescricao()
            });
        }
    }
    
        public void readJTableForDesc(String desc) {
        
        DefaultTableModel modelo = (DefaultTableModel) jProjetos.getModel();
        modelo.setNumRows(0);
        ProjetoDAO pdao = new ProjetoDAO();

        for (Projeto p : pdao.readForDesc(desc)) {

            modelo.addRow(new Object[]{
                p.getId(),
                p.getNome(),
                p.getModulo(),
                p.getFuncionalidade(),
                p.getDatacriacao(),
                p.getAutor(),
                p.getVersao(),
                p.getPrioridade(),
                p.getComplexidade(),
                p.getEsforco(),
                p.getEstado(),
                p.getFase(),
                p.getDescricao()
            });

        }

    }

