    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
        if (jProjetos.getSelectedRow() != -1) {

            Projeto p = new Projeto();
            ProjetoDAO dao = new ProjetoDAO();

            p.setNome(txtName.getText());
            p.setModulo(txtModule.getText());
            p.setFuncionalidade(txtFunc.getText());
            p.setDatacriacao(txtDateCreate.getText());
            p.setAutor(txtAuthor.getText());
            p.setVersao(txtVersion.getText());
            p.setPrioridade(txtPriority.getText());
            p.setComplexidade(txtComplexity.getText());
            p.setEsforco(txtEffort.getText());
            p.setEstado(txtState.getText());
            p.setFase(txtPhase.getText());
            p.setDescricao(txtDescription.getText());
            p.setId((int)jProjetos.getValueAt(jProjetos.getSelectedRow(), 0));
            dao.update(p);

            txtName.setText("");
            txtModule.setText("");
            txtFunc.setText("");
            txtDateCreate.setText("");
            txtAuthor.setText("");
            txtVersion.setText("");
            txtPriority.setText("");
            txtComplexity.setText("");
            txtEffort.setText("");
            txtState.setText("");
            txtPhase.setText("");
            txtDescription.setText("");

            readJTable();
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

